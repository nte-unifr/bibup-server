			<li>The ISBN code is the chapters set reference and can't be a specific chapter reference.</li>
			<li>Thus, BibUp memorises : the title, the authors, the year, ans so on, of the magazine or chapters set, but not the authors, tile and so on of the specific article or chapter.</li>
