      <h2>Description</h2>
      <p><strong>BibUp</strong> allows you to create bibliographic references by scanning books barcodes and extracts of text. The references, including the OCRed text, can be viewed on a web page and collected using the Zotero plugin for Firefox</p>
<br /><a href="#" title="Read more" class="toggler">More&raquo;</a></p>
      <div class="toggleMe">
	<p>Install BibUp from the App Store. Then, you'll be able to :</p>
	<ul>
	  <li>quickly get bibliographic references (book or magazine) using ISBN or ISSN barcodes;</li>
	  <li>add an annotation as well as a picture of a page (or part of);
</li>
	  <li>fetch the taken references from a web page;</li>
	  <li>import the whole reference (with your annotation, OCRed text (if your were using the University of Fribourg wifi) using Mozilla Firefox and the Zotero add-on;</li>
	  <li>re-use these references in a text editor such as OpenOffice Writer or Microsoft Word).</li>
	  <li>Note : BibUp gets a book or a magazine reference, but it is not possible to get a precise reference of an article or a book chapter. </li>
	</ul>
      </div>
