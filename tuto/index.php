      <h2>With Firefox on your computer</h2>
      </a>
<p><a href="#" title="Read more" class="toggler">More &raquo;</a></p>
      <div class="toggleMe">
	<p>To get the references, you need to use the web browser <a href="http://www.mozilla.com/en-US/firefox/">Firefox</a> and the bibliographic manger add-on <a href="http://zotero.org">Zotero</a> (a version for Internet Explorer is scheduled).</p>
	<p>To control if Zotero is installed on your web browser, have a look at the bottom right of a Firefox window. The word "zotero", or a red "Z" should appear.</p>
	<p>When you click it, Zotero opens up in the lower part of the Firefox window.</p>
	<p>Note : the web page containing the references is also visible with other web browsers.</p>
      </div>
