      <h3>9. Check and complete the reference</h3>
      <p>You can view your reference by opening Zotero.<br /><br /><a href="#" title="Read more" class="toggler">More &raquo;</a></p>
      <div class="toggleMe">
      	        <p>To view a reference in Zotero, click on the Zotero (or "Z") icon at the bottom right of your Fierefox window, and select the reference in the list that appears.</p>
		<p>Check if all the needed fields are correctly filled. Depending on the information retrieved, these fields can be more or less complete/correct.</p>
		<p>For articles, BibUp retrieves automatically only the journal title. An easy way to get the complete reference of an article in Zotero is to use specialized sites as <a href="http://scholar.google.ch/">Google scholar</a> or <a href="http://www.oaister.worldcat.org/">OAIster</a>. Copy the OCRed text of the title page of the article in google scholar and import the reference from there.</p>
		<p>It is also possible to export the collected references from Zotero to other reference management tools. Zotero.org has pages on that subject: <br><a href= "http://www.zotero.org/support/kb/exporting">How do I export my Zotero library?</a> <br><a href="http://www.zotero.org/support/kb/field_mappings">The different export formats</a>.</p>
