		<p>Note: we encourage you to use wifi. BibUp lets you do the work with a mobile phone network (HSPA, 3G, 2G, Edge), but if you send pictures, the waiting time could be high. Furthermore, your mobile provider might charge you (each picutre is about 3MB).
		</p>
