           <li>Touch the field "Page number or other information". The keyboard activates. </li>
           <li>Enter the desired text.</li>
           <li>To end the note :</li>
            <ul>
                  <li>Touch the "Send reference" button if you want to send the reference without picture;</li>
                  <li>or touch white space outside any field or button.</li>
            </ul>
        </ul>
	<p>Result : the whole note is memorised and linked to your bibliographic reference.</p>
