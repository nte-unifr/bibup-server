			<li>Touch on the "Enter ISBN/ISSN" button to enter the ISBN or ISSN manually.</li>
			<li>On the input field, you can manually enter ISBN or ISSN using the numerical keyboard. The numerical code has 10 or 13 digits. It is located just below or above the barcode.</li>
			<li>Please enter the ISBN or ISSN number without spaces or special signs.</li>
		</ul>
 		<p>Note : to make the numerical keyboard appear, just touch the input field near "ISBN/ISSN : * ".</p>
