      <h3>6. Add the first page</h3>
      <p><strong>Articles and chapters set</strong></p>
      <p>You can take a second picture (touch "Image 2").</p>
      <p>Since the ISBN or ISSN code doesn't give the specifics of an article or a chapter, you might need this information.<br /><br /><a href="#" title="Read more" class="toggler">More &raquo;</a></p>
      <div class="toggleMe">
      	        <p> For an article or a chapters set :</p>
		<ul>
			<li>The ISSN/ISBN is the reference to the entire journal or book, not to a specific article or chapter.</li>
			<li>Thus, BibUp memorises : the title, the authors, the year, ans so on, of the journal or book, but not the authors, tile, pages and so on of the specific article or chapter.</li>
		</ul>
		<p>In this case, you can take a snapshot or the title page of the article or chapter (you thus keep the information about the title and authors).</p>
		<p>If you use the unifr wifi when sending the reference, the snapshot will be OCRed.</p>
		<p>When using your bibliographic manager, you'll just have to cut and paste this information at the right place or find the specific reference with other online tools (see point 9. below).</p>
