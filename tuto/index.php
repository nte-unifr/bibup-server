    <div class="teamMemberRight">
      <h3>8. Save your references into Zotero</h3>
      <p>On the <a href="http://www.unifr.ch/go/bibup">BibUp web page</a>, click on the Zotero icon (yellow folder or blue file image) at the right of the address bar to save your references into Zotero.</p>
      <p>You can filter out the references by entering the tag zou used in step 2 in the "Tag" field.<br /><br /><a href="#" title="Read more" class="toggler">More &raquo;</a></p>
      <div class="toggleMe">
      	        <p>On the <a href="http://www.unifr.ch/go/bibup">BibUp web page</a>, references scanned in the last seven days by you and other users are displayed. If your references aren't already on the page, wait 5 minutes and reload the page.</p>
		<p>To save your references with their notes, snapshots (and OCRed text if you were on the intranet of UniFR) to Zotero, just click on the Zotero icon at the right of the address bar, next to the site address (http://...).</p>
		<p>After you've clicked on the "Save to Zotero" blue file icon <img src="images/Zotero_1RefLogo.png" width="21">, the reference is saved directly to Zotero.</p>
		<p>After you've clicked on the "Save to Zotero" yellow folder icon <img src="images/Zotero_moreRefLogo.png" width="25">, you can select from the list the reference(s) to save to Zotero.</p>
		<p></p>
