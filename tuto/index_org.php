      <h2>Description générale</h2>
      <p><strong>BibUp</strong> permet de saisir rapidement les références bibliographiques de livres et de revues scientifiques en utilisant le code barre du ISBN/ISSN.<br /><br /><a href="#" title="Read more" class="toggler">En savoir plus &raquo;</a></p>
      <div class="toggleMe">
	<p>Sitôt que vous avez installé l'application BibUp de l'Université de Fribourg sur votre iPhone depuis l'App Store, vous pouvez :</p>
	<ul>
	  <li>Obtenir rapidement les références bibliographiques de livres et de revues en utilisant le code barre du ISBN/ISSN.</li>
	  <li>Ajouter une annotation ainsi qu'une photo d'une page (ou d'une partie de page).</li>
	  <li>Récupérer via une page web les références récoltées avec l'iPhone.</li>
	  <li>Importer d'un simple clic la référence complète, avec votre annotation et le texte de votre photo (traité par un service de reconnaissance de caractères OCR si vous avez saisi la référence depuis le réseau intranet de l'Université de Fribourg), en utilisant le navigateur Mozilla Firefox et l'add-on Zotero (un gestionnaire de références bibliographiques gratuit et très performant)...<br /></li>
	  <li>Réutiliser ces références dans un éditeur de texte (p. ex.: OpenOffice Writer ou Microsoft Word).</li>
	  <li>Remarque : il est actuellement possible de récupérer automatiquement la référence d'une revue ou d'un livre, mais pas la référence exacte dun 'article ou d'un chapitre de livre</li>
	</ul>
      </div>
