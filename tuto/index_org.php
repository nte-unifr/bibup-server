      <h3>8. Saisir la première page</h3>
      <p><strong>Articles et ouvrages collectifs</strong></p>
      <p>Vous pouvez prendre une deuxième photo en cliquant sur "Article first page".</p>
      <p>Cela est surtout utile pour un article de revue ou un chapitre d'un ouvrage collectif, car le ISBN/ISSN ne saisit pas les indications spécifiques à l'article ou au chapitre. <br /><br /><a href="#" title="Read more" class="toggler">En savoir plus &raquo;</a></p>
      <div class="toggleMe">
      	        <p> Pour un article ou un chapitre d'un ouvrage collectif :</p>
		<ul>
			<li>Le code ISSN (le correspondant du ISBN pour les revues) n'est que la référence de la revue, mais pas de l'article.</li>
			<li>Le code ISBN est la référence de l'ouvrage collectif, mais pas du chapitre.</li>
			<li>Sont donc mémorisés : le titre, les auteurs, l'année, etc. de la revue ou de l'ouvrage collectif, mais pas les auteurs, le titre, etc. de l'article ou du chapitre.</li>
		</ul>
		<p>Dans ce cas, vous pouvez prendre en photo la page de titre de l'article/chapitre (avec son titre et ses auteurs).</p>
		<p>Si vous êtes connectés au réseau de l'Université de Fribourg au moment de l'envoi de la référence, cette page sera traitée par un OCR.</p>
		<p>Après importation dans votre gestionnaire bibliographique, il vous suffira de copier-coller ces informations dans les bons endroits.</p>
